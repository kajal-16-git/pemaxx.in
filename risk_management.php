        <p class="normal" style="text-align: justify;">Risk management, also known as money management, refers to a number of trading techniques employed to lessen risk exposure. Being affected by various factors, currency rates may be quite volatile at times, thus protecting your account against adverse price fluctuations is an essential part of a <a class="btn-t48 tutorials__btn-link" style="color: #ff561c;" href="trading_strategies.php" target="_blank">trading strategy.</a></p>
        <p class="normal" style="text-align: justify;">The core concept of money management is to avoid risking more than 1-2% of personal funds on any single trade. This principle may greatly reduce risk exposure: provided that only 1% of initial deposit is at risk, even after several losing trades you are likely to retain the majority of account balance.</p>
        <p class="normal" style="text-align: justify;">Risk to reward ratio denotes the potential profit in comparison to the amount you may lose for any given trade. For example, when you risk 100 USD on position to potentially gain 300 USD, the risk to reward ratio is 1:3.</p>
        <p class="normal" style="text-align: justify;">Ratio of 1:2 is considered the minimum one should aim for as only a third of positions would need to be profitable to remain break even.</p>
        <p class="normal" style="text-align: justify;">Potential profit and loss can be defined through stop-loss and take-profit levels.</p>
        <p class="normal" style="text-align: justify;">
            Stop loss and take profit are orders to close the position when price reaches a certain predefined level. Stop-loss or take-profit level can be identified with various <a class=" tutorials__btn-link" style="color: #ff561c;" href="technical_analysis.php" target="_blank"> technical analysis</a>  tools
            :
        </p>
        <ul class="rules" style="list-style-type: circle; text-align: justify;font-size: 15px;">
            <li>Support and resistance: for a short position stop loss is usually placed just above resistance level, while a long position often has stop loss set a little below support level.</li>
            <li>Trend lines and channels: stop loss price is commonly placed outside the channel, above or below the trend line.</li>
        </ul>
        <p class="normal" style="text-align: justify;">Let &rsquo;s say you open 1 lot EURUSD Buy order at 1.12097. To achieve risk to reward ratio of 1:2, you can set stop loss level at 1.12077 (2 pips)&nbsp;and take profit level at 1.12137 (4 pips). Thus, you will only be risking 20 USD to gain 40 USD. Depending on your initial deposit, you can set SL/TP levels even further, as long as your risk is below 1-2% of the personal funds.</p>
        <p class="normal">
            <img style="display: block; margin-left: auto; margin-right: auto; height: 350px;
    width: 529px;" src="assets/images/gallery/art_6.png" alt="How to identify Stop loss and Take profit levels" width="100%"/>
        </p>
        <p class="normal" style="text-align: justify;">
            It is important to note that the price of each pip depends on the trading tool and the volume of your position. You can find pip price per 1 lot on the <a href="spreadsandconditions.php" style="color: #ff561c;">Spreads and Conditions</a>
            page or simply calculate it here.
        </p>
        <p class="normal" style="text-align: justify;">Trailing Stop can be used to adjust stop-loss level automatically whenever the price moves in a favourable direction. Along with reducing the risks, it may also eventually lock in the profit already gained.</p>
        <p class="normal" style="text-align: justify;">Keep in mind, however, that neither stop loss nor take profit is guaranteed: when the market is volatile or during a price gap your order may be executed at a different price than expected.</p>
        <p class="normal" style="text-align: justify;">
            You can learn more about events and indicators that affect market volatility.
            
