        <h2 style="font-size: 35px;">What is ECN/STP trading?</h2>
        <p>It is a broker's business model in which clients` orders are sent directly to one or several liquidity providers to be executed on their end. Liquidity providers include companies, banks or financial institutions that quote both buy and sell price in a financial instrument or commodity.&nbsp;</p>
        <p>The more liquidity providers a broker has in general, the better execution for its clients &nbsp;will be (more liquidity available generally means less price slippage). What makes a true STP (Straight through processing) broker is that the STP broker doesn &rsquo;t internalise the orders, but sends them to liquidity providers, acting as an intermediary between their client and the real market.</p>
        <p>
            <img src="../../../pics/a/Education/About%20ECN%20trading/en/1.jpg" alt="" width="100%"/>
        </p>
        <h2 style="font-size: 25px;">Do you have requotes?</h2>
        <p>No, we don't. Any broker who re-quotes your orders is a dealing desk broker. A requote occurs whenever the dealer on the other side of the trade (whether human or automatic) sets an execution delay during which the price changes. Therefore the broker can &rsquo;t open your order and sends you a message that the price has changed. That is, a requote. You usually get a new price which can be different from the one you requested (especially when the market is volatile). Often the requote is not an improvement for the client.&nbsp;Pemaxx doesn't have any requotes simply because we don't have a dealing desk, human or automatic (a piece of software usually referred to as a virtual dealer, automatic dealer and so on).</p>
        <h2 style="font-size:25px;">Can I scalp? Do you allow news trading?</h2>
        <p>Yes, you can. Unlike some brokers who prohibit scalping,&nbsp;Pemaxx  welcomes scalpers. Dealing desk brokers hold the other side of client trades, and have to decide whether to hedge or run their client &rsquo;s overall net position at any given moment. Therefore trading styles such as momentum scalping can make it difficult for dealing desk brokers to manage client positions, particularly as scalpers generally open and close trades relatively quickly.</p>
        <p>Another potential issue for dealing desk brokers is that scalpers generate a proportionately large number of requests to trade at the same time during busy periods, for example during the release of key economic data, which above a certain trade size are generally handled individually and can lead to an increased number of requotes for clients.</p>
        <p>Pemaxx  &nbsp;are not a dealing desk broker. Instead all the trades are passed to our liquidity providers. The larger the volume of orders to trade we receive, the better it is for us, as we receive a commission based on trade volume.</p>
        <h2 style="font-size:25px;">How do I find out if my broker is a dealing desk?</h2>
        <p>Indicators could be:</p>
        <ul class="rules" style="list-style-type: circle;">
            <li>A direct or indirect prohibition of scalping, news trading, or some other similar strategies</li>
            <li>Fixed spreads</li>
            <li>So-called "guaranteed" stop orders</li>
            <li>A possibility of requotes</li>
        </ul>
        <p>If you encounter any of these, the broker is quite likely to be a dealing desk broker. Dealing desks brokers (also known as "market makers") create their own markets based on the underlying market. NDD (No Dealing Desk) brokers such as &nbsp;Pemaxx  act as intermediaries between the trader and the real market, and receive a defined and transparent commission for it.</p>
        <h2 style="font-size:25px;">How do dealing desks earn?</h2>
        <p>They earn the difference between overall client losses and client gains that aren &rsquo;t hedged. In general dealing desk brokers experience a two way buying and selling client flow in a given <a class="btn-t48 tutorials__btn-link" style="color: #ff561c;" href="live_market.php" target="_blank">market.</a>  Dealing desk brokers need to manage the net position of the flow, whether long or short, at any given moment. Depending on the broker, a portion may be hedged in the real market and the remaining exposure, up to the brokers <a class="btn-t48 tutorials__btn-link" style="color: #ff561c;" href="risk_management.php" target="_blank">risk management</a> limit, run naked as a trade of the broker in its own right.</p>
        <h2 style="font-size:25px;">How does &nbsp;Pemaxx  make money?&nbsp;Pemaxx  needs profitable traders? WHY?</h2>
        <p>Pemaxx  &nbsp;receives a commission from its liquidity providers for each transaction.</p>
        <p>We receive our liquidity from a wide range of liquidity providers around the world. Our system is designed to offer the best aggregated prices of our liquidity providers direct to our clients. When you open a new order, you get the best available bid (or ask) price which is available from our liquidity providers with our commission already included in the spread you see on the trading platform. Therefore we are interested in you trading more, and staying with us as our client. Therefore it &rsquo;s in our interest that your trading is as profitable as possible.</p>
        <h2 style="font-size:25px;">You have no requotes. WHY?</h2>
        <p>Putting it simply, we don't requote you because we have nothing to do with the quotes (i.e. the prices you see in your trading software). The order is filled when a price from one of our liquidity providers is available. It is important to understand, however, that we do not guarantee that your order will be filled exactly at the requested price; our system is setup to fill it by the next best price from another liquidity provider. But, again, your order will not be requoted, since we are more interested in your profitable trading.</p>
        <h2 style="font-size:25px;">Can liquidity providers see my orders?</h2>
        <p>No, they can't. From their point of view they see only one customer, that is, Pemaxx . You remain anonymous to them in all cases.</p>
        <h2 style="font-size:25px;">The chart went through my limit, but my order wasn't opened. What's going on?</h2>
        <p>It is a possibility, and usually happens due to a lack of liquidity at a given time. For example, a number of clients place sell limit orders above the market prior to an important news release with a total volume of 1000 lots. When the news is released, the market goes up 50+ pips to where the chart hits the price of all these orders and requests are electronically made to open a number of orders worth 1000 lots in total. It may happen that only 200 lots are available from the liquidity providers at this price and at this given time. In this case the first 200 lots out of 1000 will be filled, while the remaining 800 will not be filled (no available liquidity) and will remain pending until the price hits the level or beyond again.</p>
        <h2 style="font-size:25px;">Do you allow Expert Advisors (EA &rsquo;s)?</h2>
        <p>Absolutely. All Expert Advisors (EA's) are welcome.</p>
        <h2 style="font-size:25px;">What is slippage and why does it happen?</h2>
        <p>Slippage is a slight order opening price movement which is a result of lack of liquidity (when it's already taken by other traders' orders). It may also happen during market gaps.</p>
        <p>
            <img src="../../../pics/a/Education/About%20ECN%20trading/en/2%20en.jpg" alt="" width="100%"/>
        </p>
        <p>Slippage is an order execution price difference which can be a result of a lack of liquidity or speed (Other traders have got there first). It may also happen due to gaps in the pricing of a market.</p>
        <p>It is important to understand that &nbsp;Pemaxx  do not guarantee that your order will be filled exactly at the requested price; our system is setup to fill orders with the next best price available from the liquidity providers when slippage occurs.</p>
        <p>So during these news times it's possible that there won &rsquo;t be liquidity available at the price you requested. For example you want to open a 5 lot Buy order, EUR/USD, price is 1.30000. Now, in this case we can see the following liquidity available on the basic illustration above:</p>
        <p>Provider 1: price is 1.30010, 20 lots available</p>
        <p>Provider 2: price is 1.30005, 5 lots available</p>
        <p>Provider 3: price is 1.30000, 1 lot available</p>
        <p>In this case your order will be offset with Provider 2, since he has the best price and enough liquidity to fill your order. And the open price will be 1.30050, which is 0.5. pips away from the price you requested. But, again, your order will not be requoted, since we are more interested in your profitable trading <a class=" tutorials__btn-link" style="color: #ff561c;" href="trading_education.php" target="_blank"> here</a>.</p>
        <h2 style="font-size:25px;">Why don't you guarantee stop orders?</h2>
        <p>In the real market there is no such thing as a "guaranteed stop". They are offered by dealing desk brokers who create synthetic markets based on the underlying market. As dealing desk brokers generally run a proportion of the net client positions as an in-house trade against the clients, and the market is an in-house market, they have greater flexibility on stops. Guaranteed stops are typically set by the client at point of execution, can rarely be moved and incur a charge of additional spread to enter the initial trade.</p>
        <p>In the real market any stop order is considered pending until its price is hit. After that the order is offset to a liquidity provider which may or may not involve slippage depending on the available liquidity. Therefore it's impossible to "guarantee" stop orders in the real market.</p>
