        <p dir="ltr" style="text-align: justify;">
            
                <span>Moving averages are among the most widely used and efficient indicators for technical analysis. Traders of all levels determine long term trends using this tool. We &rsquo;ll cover how to define a trend, how to implement this indicator for support and resistance levels, and how to use it as trade entry and exit signal. </span>
            
        </p>
        <h3 style="text-align: justify; font-size: 35px;">
            What are Moving Averages?
        </h3>
        <p dir="ltr" style="text-align: justify;">Moving averages (MA) indicate the average trend within the chosen period, smooth out price action and filter out the noise. They are formed using the average closing price over a set number of periods. For example, a ten-day simple moving average is a total of the closing prices during a period of ten days divided by ten. <a class="btn-t48 tutorials__btn-link" style="color: #ff561c;" href="learn more.php" target="_blank">MetaTrader 5</a></p>
        <p dir="ltr" style="text-align: justify;">The most commonly used moving averages are:</p>
        <ul style="list-style-type: circle; text-align: justify;">
            <li dir="ltr">
                <p dir="ltr">Simple Moving Average (SMA), which indicates the average movement of price during a particular time period;</p>
            </li>
            <li dir="ltr">
                <p dir="ltr">Exponential Moving Average (EMA), which is similar to SMA, but applies a greater weight to more recent prices.</p>
            </li>
        </ul>
        <h3 style="text-align: justify;font-size: 35px;">
            Adding Moving Averages to your chart in MetaTrader 4
        </h3>
        <p dir="ltr" style="text-align: justify;">You can add moving averages to your chart simply by clicking the &lsquo;indicators list &rsquo;icon in the toolbar and selecting &lsquo;moving average &rsquo;. From there you can choose the period and the type of MA you want to work with. If you &rsquo;re using multiple moving averages you can change the color in order to identify them easily.</p>
        <p dir="ltr" style="text-align: justify;">
            <strong>
                <img src="../../../pics/a/news/Moving%20averages/EN/firstMA.png" alt="" width="100%"/>
            </strong>
        </p>
        <h3 style="text-align: justify;font-size: 35px;">
            Simple Moving Averages vs. Exponential Moving Averages
        </h3>
        <p dir="ltr" style="text-align: justify;">SMA are slower than EMA in responding to price action. Consequently, they indicate trends or trend reversals more slowly, but are less prone to false signals. EMA are better at catching trends early, though they will give you more false signals during consolidation periods.</p>
        <p dir="ltr" style="text-align: justify;">Using several types of moving averages will give you a broader outlook. For instance, a trader might choose longer period SMA to help understand the broader trend, and shorter period EMA for entry signals.</p>
        <p dir="ltr" style="text-align: justify;">SMA may better identify support and resistance levels since they reflect a balanced average of prices over a set time period. <a class="btn-t48 tutorials__btn-link" style="color: #ff561c;" href="trading_education.php" target="_blank">here</a></p>
        <h3 style="text-align: justify;font-size: 35px;">
            How to determine a trend using MA?
        </h3>
        <p dir="ltr" style="text-align: justify;">When a price action remains above a moving average, it signals that the price has an uptrend. Conversely, if a price action stays below the MA it indicates a downtrend.</p>
        <p dir="ltr" style="text-align: justify;">Using several MA simultaneously can be beneficial in determining a trend. Make sure the MA lines are set in the correct order (from fastest to slowest in an uptrend, from slowest to fastest in a downtrend) to confirm that you are in a strong uptrend or downtrend.</p>
        <p dir="ltr" style="text-align: justify;">
            <strong>
                <img src="../../../pics/a/news/Moving%20averages/EN/downtrend.png" alt="" width="100%"/>
            </strong>
        </p>
        <h3 style="text-align: justify;font-size: 35px;">
            Moving Average Crossovers
        </h3>
        <p dir="ltr" style="text-align: justify;">MA crossovers help to determine when a new trend starts and when an existing one is about to reverse.</p>
        <p dir="ltr" style="text-align: justify;">Upward momentum is confirmed with a bullish crossover, when short-term MA crosse above longer-term MA. This can be called a &lsquo;Golden Cross &rsquo;. Downward momentum is confirmed with a bearish crossover, when short-term MA cross below longer-term MA, also known as a &lsquo;Death Cross &rsquo;. Short term MA can be used for intraday trading, for instance, EMA with 5 and 10 periods.</p>
        <p dir="ltr" style="text-align: justify;">
            <strong>
                <img src="../../../pics/a/news/Moving%20averages/EN/crossover.png" alt="" width="100%"/>
            </strong>
        </p>
        <p dir="ltr" style="text-align: justify;">Longer-term traders might prefer SMA crossovers with 50 and 200 periods.</p>
        <p dir="ltr" style="text-align: justify;">On one hand, MA crossovers tend to work well in trending markets, while on the other, they can be worthless in sideways or consolidating <a class="btn-t48 tutorials__btn-link" style="color: #ff561c;" href="live_market.php" target="_blank">live market.</a>.</p>
        <h3 style="text-align: justify;font-size: 35px;">
            Moving Averages as Dynamic Support and Resistance Levels
        </h3>
        <p dir="ltr" style="text-align: justify;">MA can also be used as support in an uptrend and resistance in a downtrend. For example, a short-term uptrend might find support near the 21-day EMA, while a long-term uptrend might find support near the 200-day SMA.</p>
        <p dir="ltr" style="text-align: justify;">
            <strong>
                <img src="../../../pics/a/news/Moving%20averages/EN/support_resistance.png" alt="" width="100%"/>
            </strong>
        </p>
        <p dir="ltr" style="text-align: justify;">50 and 200-period SMA are mostly followed on the daily chart and often represent key support and resistance levels. Keep in mind that the levels are not exact, and MA more often represent areas of support and resistance. Levels are also dynamic, as they are constantly changing based on recent price action.</p>
        <p dir="ltr" style="text-align: justify;">
            MA is a multi-faceted indicator that can help you to define a trend, support and resistance. It can also show when a new trend starts and when a trend is about to reverse. Set different periods of MA to determine what works best for your  <a href="trading_strategies.php" style="color:#ff561c;">trading strategy.</a>
        </p>
