    <p>A position can either be closed manually at the current market rate or when a certain price level is reached, through stop-loss and take-profit orders.</p>
    <ul>
        <li>
            <p>Stop loss is intended to limit potential losses and is set above the open price for short positions and below the open price for long positions.</p>
        </li>
        <li>
            <p>Take profit allows you to close a position when a certain profit is gained. The take profit level should be set below the current ask price for a short position and above the current bid price for a long position.</p>
        </li>
    </ul>
    <p>In order to gain profit, you need to close long positions when the price goes up and short positions when the price goes down.</p>
