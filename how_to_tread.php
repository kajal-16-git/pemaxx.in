<section>
    <h3 id="leverage_volume_required_margin" style="font-size:35px;">Leverage, volume, and required margin</h3>
    <p>To open a position, you need to have a certain amount in your balance, which is commonly referred to as required margin or just margin. The amount needed depends on the trading instrument, volume, and leverage.</p>
    <ul>
        <li>
            <p>Trading instruments are essentially anything you can trade, including currency pairs, metals, oil, or indices.</p>
        </li>
        <li>
            <p>
                Volume is the amount you buy or sell measured in lots. One standard lot equals 100,000 units of the base currency. Depending on your balance and account type, you can also trade mini lots (0.1) and micro lots (0.01). Volume defines the pip price, that is, the higher your volume is, the more significant each price movement will be. For example, the pip price for one lot of EURUSD is 10 USD, and for 0.5 lots of EURUSD is 5 USD. You can use this tool
                to calculate pip price for any position.
            </p>
        </li>
        <li>
            <p>Leverage is a virtual credit provided by the broker. The higher your leverage is, the lower your marginal requirements will be. For example, when you use no leverage (ratio 1:1), you will need 100,000 EUR to open an order for 1 lot of EURUSD; if your account leverage is 1:200, only 500 EUR will be required. The maximum leverage Pemaxx offers is 1:1000, that is, you will need only 100 EUR to open an order for 1 lot.</p>
        </li>
    </ul>
</p>
