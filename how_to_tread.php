<p>Note that if you have a USD account, the required margin will be calculated as follows:</p>
<p>(Current price &times;Volume in lots &times;100 000 units) / leverage</p>
<p>For example, if your leverage is 1:200 and you open 0.5 lot EURUSD order at 1.12931, required margin is</p>
<p>(1.12931 &times;0.5 lots &times;100 000 units) / 200 = 282.33 USD</p>
<p>
