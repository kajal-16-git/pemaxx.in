<section>
    <h3 id="currency_pairs_and_rates" style="font-size:35px;">Currency pairs and rates</h3>
    <p>All currencies in Forex trading are quoted in pairs, one against another. Their names are given as a three-letter abbreviation known as an ISO code, where the first two letters represent the country and the third one is the name of the currency.</p>
    <p>Depending on how commonly they are traded, currencies can be divided into three categories:</p>
    <ul>
        <li>
            <p>The most traded ones are usually referred to as majors and include the U.S. dollar, the euro, the Great Britain pound, the Japanese yen, the Canadian dollar, the Swiss franc, the Australian dollar, and the New Zealand dollar. Major pairs are between the U.S. dollar and one of the other currencies from the list above, for example EURUSD, USDJPY, and USDCHF.</p>
        </li>
        <li>
            <p>Cross pairs consist of two major currencies, neither of which is the U.S. dollar, such as EURGBP, EURCHF, EURJPY, GBPCAD, GBPAUD, and CHFJPY.</p>
        </li>
        <li>
            <p>Exotic pairs consist of a major currency and another less traded one, for instance, EURTRY, USDSEK, USDDKK, USDHDK, and USDSDG. Exotics tend to be less liquid, meaning, they are harder to buy or sell and have wider spreads.</p>
        </li>
    </ul>
