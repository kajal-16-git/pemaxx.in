<section>
    <h3 id="balance_equity_free margin_margin_level" style="font-size:35px;">Balance, equity, free margin, and margin level</h3>
    <p>When you open a position, note that your balance remains intact. In fact, it only includes <a class="btn-t48 tutorials__btn-link" style="  color: #ff561c;" href="depositswithdrawals.php" target="_blank">deposits ,withdrawals</a>  and closed trades.</p>
    <p>The amount of required margin will be deducted from the &#39;Free margin &#39;field, which also comprises your floating profit or loss and deposit bonus if you claimed one. Free margin is the funds you can open positions with. Note that when you open a hedge order with the same volume, no margin will be required; however, if your free margin is negative, you will not be able to open an opposite position.</p>
    <p>Free margin = balance &ndash;required margin + floating profit / loss (+ deposit bonus)</p>
    <p>Another value affected by your profit or loss is equity, which is calculated as follows:</p>
    <p>Equity = balance + floating profit / loss (+ deposit bonus)</p>
    <p>Equity is important because, along with the required margin, it determines your margin level:</p>
    <p>Margin level = equity / required margin &times;100%&nbsp;</p>
    <p>If your margin level falls under 15%, your open positions will be mandatory closed starting with the trade that has the highest floating loss.</p>
    <p>Balance, equity, free margin, and margin level are calculated automatically by the platform and are available anytime in the &#39;Trade &#39;tab.</p>
</section>
