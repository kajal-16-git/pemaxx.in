        <p style="text-align: justify;">You might have heard a lot about precious metals &mdash;professionals unanimously declare them a must for every trading or investing portfolio.</p>
        <p style="text-align: justify;">The reason is simple: precious metals are physical commodities that have innate value because of their rarity, limited reserves, and usefulness. A person who knows how to use gold and silver correctly can secure their investments both in the short and long term. This safeguarding can give you a reasonably good advantage in a rapidly changing world.</p>
        <h2 style="font-size: 35px;">Let's start with the basics</h2>
        <p style="text-align: justify;">Among all precious metals, gold (XAUUSD) and silver (XAGUSD) are the most popular. Both commodities are traded on the United States Commodity Exchange Inc. (COMEX). Price quotations are measured in U.S. dollars per troy ounce. One troy ounce equals 31.10 grams.</p>
        <p style="text-align: justify;">In Forex, measuring is a little different: you do not buy troy ounces, as gold there is a form of currency. Instead, gold volume is measured in lots. One standard lot of gold or silver contains 100 units. As your broker, we allow you to trade smaller volumes: mini lots of a 0.1 standard lot or micro lots of a 0.01 standard lot. Trading mini and micro lots requires less margin and less money to invest<a class=" tutorials__btn-link" style=" color: #ff561c;" href="trading_education.php" target="_blank"> here</a>.</p>
        <h2 style="font-size: 35px;">Four main features of gold and silver</h2>
        <ul>
            <li style="text-align: justify;">
                <strong>Their value rises in a crisis.</strong>
                Both precious metals are considered safe-haven assets. Demand for gold and silver rises during crises and times of economic uncertainty. Unlike currencies and company shares, gold does not depend on any particular issuer or state.
            </li>
            <li style="text-align: justify;">
                <strong>Metals protect your money from inflation.</strong>
                Investors often use gold and silver to hedge against inflation. The value of precious metals increases when the purchasing power of fiat currencies declines. However, metals prices decline when central banks pursue hawkish monetary policy because assets are sensitive to changing interest rates.
            </li>
            <li style="text-align: justify;">
                <strong>Both metals are good diversification instruments.</strong>
                Investing in different instruments helps <a class="btn-t48 tutorials__btn-link" style="color: #ff561c; font-size: 20px;" href="risk_management.php" target="_blank">risk management</a>   and protects funds from running losses. Gold and silver are less risky than other assets, so their prices will not be affected as much if something goes wrong.
            </li>
            <li style="text-align: justify;">
                <strong>Neither brings any passive income.</strong>
                Metals are non-yielding assets because they do not provide regular income, such as dividends or interest, unlike stocks, bonds, or savings accounts. Still, investors appreciate precious metals because their value rises in the long run.
            </li>
        </ul>
        <h2 style="font-size: 35px;">How do traders benefit from gold?</h2>
        <ul>
            <li style="text-align: justify;">
                <strong>Quick buying and selling.</strong>
                Gold is a highly liquid instrument, meaning you can instantly buy and sell the asset if you want to.
            </li>
            <li style="text-align: justify;">
                <strong>Stable prices.</strong>
                XAUUSD can be influenced by global news and economic data releases, but its price does not fluctuate as rapidly as that of stocks, cryptocurrencies and other risky assets. In times of economic uncertainty, investors use gold to save their funds.
            </li>
            <li style="text-align: justify;">
                <strong>Knowing their funds are safe.</strong>
                Central and local banks keep a part of their funds in gold and build up reserves in times of crisis. The more the trust in major reserve currencies decreases, the higher the gold value rises.
            </li>
        </ul>
        <h2 style="font-size: 35px;">What's good about silver?</h2>
        <p style="text-align: justify;">Silver is a much less popular asset than gold, but it can be a great addition to investors' portfolios, especially when gold is too expensive to trade.&nbsp;</p>
        <ul>
            <li style="text-align: justify;">
                <strong>Higher potential profit in a shorter time.</strong>
                Silver is more volatile than gold. Therefore, traders with a higher risk profile may earn more pips in a short time frame, setting wider stop-loss and take-profit levels.
            </li>
            <li style="text-align: justify;">
                <strong>Lower price.</strong>
                XAGUSD is much cheaper than XAUUSD, so it's a great alternative for investors with a small budget. Over the past four years, gold's price has been around 80 times higher than silver's.
            </li>
            <li style="text-align: justify;">
                <strong>Wide industrial demand.</strong>
                Silver is commonly used to manufacture electric cars, solar panels, and electronics. Industrial demand for silver is much bigger than for gold or any other precious metal. Thus, developing technologies can increase the asset's price.
            </li>
            <li>
                <strong>Lower margin requirement.</strong>
                The margin requirement
                for XAGUSD is around 38% lower than for XAUUSD. It means traders with a limited budget can easily trade silver.&nbsp;
            </li>
            <li>
                <span id="docs-internal-guid-02a1162a-7fff-7aea-164c-1c04d67134c1">However, gold is much more expensive than silver: </span>
            </li>
        </ul>
