        <p class="normal" style="text-align: justify;">
            Fundamental analysis is the study of how economy of the country affects its currency rate, which mainly involves interpretation of statistical reports and economic indicators. Hundreds of economic news and reports released daily allow, to some extent, to predict whether the currency value will appreciate or depreciate in future and when reversal of the current trend
            may be expected.
        </p>
        <p class="normal" style="text-align: justify;">
            Date and time when a particular report or indicator due to be released is scheduled in advance and can be found in the Economic Calendar
            . It is the main tool analysts use to determine the impact news may have. It also shows experts forecasts of the data to be announced.
        </p>
        <h3 style="text-align: justify; font-size: 37px;">Central Bank and Interest Rates</h3>
        <p class="normal" style="text-align: justify;">Since a central bank is often responsible for handling country's financial matters, the policy it is pursuing has a profound impact on the currency rates. For instance, to increase the value it can buy the currency and hold it in its reserves. In order to decrease the rate, the reserves are sold back to the <a class="btn-t48 tutorials__btn-link" style="color: #ff561c;" href="live_market.php" target="_blank">market.</a> .</p>
        <p class="normal" style="text-align: justify;">When an increase in consumer spending is required, the Central bank may lower &nbsp;interest rate on the loans it provides to commercial banks. If it aims to slow inflation, the interest rates are increased in order to reduce spending.</p>
        <p class="normal" style="text-align: justify;">Depending on whether is it more concerned on inflation or growth, central bank's policy can be referred as to "hawkish" or "dovish". The former usually leads to higher interest rates, while the latter commonly signifies that the interest rate are about to be decreased.</p>
        <p class="normal" style="text-align: justify;">
            <!-- <img style="display: block; margin-left: auto; margin-right: auto;" src="assets/images/gallery/central_banks.png" alt="Central Banks around the world" width="100%"/> -->

            <img style="display: block; margin-left: auto; margin-right: auto;height: 355px;width: 690px;" src="assets/images/gallery/central_banks.png" alt="Forex Line - Bar - Candlestick charts" width="600" height="300"/>
        </p>
        <h3 style="text-align: justify; font-size: 37px;">Inflation</h3>
        <p class="normal" style="text-align: justify;">Inflation evaluates how fast the price of goods and services is rising and has a direct impact on the supply and demand for currency and thus affects its rate. The major inflation indicators are:</p>
        <ul class="rules" style="text-align: justify; list-style-type: circle;">
            <li>
                <strong>Gross Domestic Product (GDP)</strong>
                <br/>
                GDP evaluates all goods and services produced during the reporting period. An increase in GDP signifies economy growth and therefore it is used to measure inflation. <br/>
               
            </li>
            <li>
                <strong>Consumer Price Index (CPI)</strong>
                <br/>
                CPI measures the value of defined basket of goods and services expressed as an index. When compared to the previous results, CPI shows how consumer buying power has changed and how it was affected by inflation.<br/>
               .
            </li>
            <li>
                <strong>Producers price index (PPI)</strong>
                <br/>
                This indicator shows the changes in the prices that producers received and allows to evaluate how the consumer level price could be affected.<br/>
                
            </li>
        </ul>
        <h3 style="text-align: justify;font-size: 37px;">Employment</h3>
        <p class="normal" style="text-align: justify;">Employment level directly affects currency rate for it impacts future and current spending. An increase in unemployment is believed to signify that the economy is growing weaker, thus the demand for its currency is falling. On the contrary, strong employment numbers are a sign of growing economy that usually means that the demand for currency will continue to increase.</p>
        <p class="normal" style="text-align: justify;">Below you will find the most important employment reports from different countries:</p>
        <ul class="rules" style="text-align: justify; list-style-type: circle;">
            <li class="normalCxSpMiddle">US Non-Farm payroll - an assessment of employment trends with the exception of government, non-profit organisations and farm workers.</li>
            <li class="normalCxSpMiddle">US Unemployment Insurance Initial Claim - the number of new unemployment benefits claims that measured the number of newly unemployed.</li>
            <li class="normalCxSpMiddle">Labor Force Survey - measures the changes of current employment rates in Canada.</li>
            <li class="normalCxSpMiddle">Wage Price Index - indicates changes in wages in Australia.</li>
            <li class="normalCxSpMiddle">Claimant Count Change - measures Unemployment Insurance claims changes from one reporting period to another in the UK.</li>
        </ul>
        <h3 style="text-align: justify;font-size: 37px;">Retail Sales</h3>
        <p class="normal" style="text-align: justify;">This indicator is important since consumer spending accounts for a substantial part of the economy. It measures the total amount spent on various groups of goods and services during a certain period of time. Retails sales growth shows that the consumers have extra income to spend and are confident in the country's economy.</p>
        <p class="normal" style="text-align: justify;">
            <!--<em>Released: Monthly, approximately mid-month; time: 15.30 EET (14.30 EEST).</em>-->
        </p>
        <h3 style="text-align: justify; font-size: 37px;">Home Sales</h3>
        <p class="normal" style="text-align: justify;">Growing housing market is one of the major indicators of a strong economy. Mainly based on the consumer confidence and mortgage rates, home sales reports show the aggregate demand among consumers for housing.</p>
        <p class="normal" style="text-align: justify;">
            <!--<em>Released: Fourth week of the month; time: 15.30 EET (14.30 EEST).</em>-->
        </p>
        <h3 style="text-align: justify; font-size: 37px;">Wholesale Trade Report</h3>
        <p class="normal" style="text-align: justify;">Wholesale Trade Report is based on the survey of 4500 wholesale merchants conducted on a monthly basis that includes statistics on monthly sales, inventories and inventory to sales ratio. It indicates imbalances in supply and demand and may help to predict quarterly GDP report, however, does not strongly impact the market.</p>
        <p class="normal" style="text-align: justify;">Released: On or around the 9th of the month; time: 17.00 EET (16.00 EEST)</p>
        <h3 style="text-align: justify; font-size: 37px;">Balance of Payments (BOP)</h3>
        <p class="normal" style="text-align: justify;">Balance of  <a class="btn-t48 tutorials__btn-link" style="color: #ff561c;" href="paymentoption.php" target="_blank">paymentoption</a> summarizes all transactions for a certain period of time between country's residents and non-resident. All transactions are subdivided into current account that includes goods, services and income, and capital account comprising of transactions in financial instruments. These data are crucial in formulating national and international economic policy.</p>
        <p class="normal" style="text-align: justify;">
            <!--<em>Released: around 19th of the month; time: 15.30 EET (14.30 EEST)</em>-->
        </p>
        <h3 style="text-align: justify; font-size: 37px;">Trade Balance</h3>
        <p class="normal" style="text-align: justify;">The report shows the difference between a country's imports and exports and is a significant part of balance of payments. Trade deficit means that the country imports more than it exports, while trade surplus indicates the opposite. Surplus or declining deficit often signifies increased demand for the currency.&nbsp;</p>
        <p class="normal" style="text-align: justify;">
