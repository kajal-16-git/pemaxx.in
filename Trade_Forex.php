        <h3 style="text-align: justify;font-size: 37px;">
            <strong>Risks in Trading the News on Forex</strong>
        </h3>
        <p style="text-align: justify;">
            Bid-offer spreads typically widen before major news releases. This increases your cost in getting in and out of the market.<br/>
            <br/>
            Slippage is another challenge. Slippage occurs when you try to enter the market at a certain price, but due to the extreme volatility after a news release, you actually get filled at a worse price.<br/>
            <br/>Volatility is also a major challenge. Even if you are right about the direction of the market, sometimes the swings are so wild that you can get stopped out of your position 
