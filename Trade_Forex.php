        <p style="text-align: justify;">We &rsquo;ll cover the fundamental price driver in the Forex market: economic news releases. Leading banks, hedge funds and retail traders all look to the news for making their trading decisions.</p>
        <h3 style="text-align: justify;font-size: 37px;">
            <strong>Why Trade the News on Forex?</strong>
        </h3>
        <p style="text-align: justify;">
            Countries around the globe regularly publish statistics tracking areas such as their labour markets, gross domestic product (GDP), retail sales and inflation.<br/>
            <br/>This news provides fresh information on how an economy is performing and strongly influences the price of currencies. Currencies are effectively confidence indicators for countries, so news releases often trigger a high level of volatility in the forex market, creating a variety of opportunities for <a href="Forex_Trading.php" style="color:#ff561c;">forex traders.</a>
        </p>
        <h3 style="text-align: justify;font-size: 37px;">
            <strong>Most Important News Releases for Forex Trading</strong>
        </h3>
        <p style="text-align: justify;">
            Economic news releases vary in the extent to which they impact the market. Economic news from the United States typically moves the market the most, since the U.S. dollar is part of about 90% of all currency transactions.<br/>
            <br/>
            The most important economic releases for any country are interest rate decisions from central banks, retail sales, inflation figures, unemployment and industrial production.<br/>
            <br/>The <a class="btn-t48 tutorials__btn-link" style="color: #ff561c;" href="live_market.php" target="_blank">forex market&rsquo;s</a>  initial reaction to a news release usually lasts from 30 minutes to two hours, but the broader impact can last for days.
        </p>
        <h3 style="text-align: justify;font-size: 37px;">
            <strong>Which Currency Pairs to Trade on Forex</strong>
        </h3>
        <p style="text-align: justify;">
            The major currency pairs (EUR/USD, GBP/USD, USD/JPY, USD/CHF) are the most liquid and have the tightest spreads, and for this reason they make a good place to start in trading news
            releases. This helps to minimise your risk in periods of high volatility.
        </p>
        <h3 style="text-align: justify;font-size: 37px;">
            <strong>Using an Economic Calendar for Forex Trading</strong>
        </h3>
        <p style="text-align: justify;">
            <span id="docs-internal-guid-263248f2-7fff-41f4-df0a-e14572f506a4">
                <img src="https://lh5.googleusercontent.com/JTvkhBWA6iQXovZxHYBksstPWJxLxj0pNC4APEZbmytLQt0nWNhL4yXw4hM-2W8Tjet36O20Qk633Hm_d-MC2dFqWR88tyPMiKdzMWcSCXwHVdTheWP6kwcjS1ayGn1XyR6XHA6AkNfUFEFNts0obB0" alt="" width="100%"/>
            </span>
        </p>
        <p style="text-align: justify;">
            Pemaxx global economic-calendar economic calendar
            is available online, listing the economic news events of the day. Each release is rated as and high, medium or low impact.<br/>
            <br/>
            Additionally the calendar will list the analyst forecast (consensus number) and the result of the previous release.<br/>
            <br/>
            Forex traders will watch to see if the actual data hits, misses or exceeds the forecast level. Typically, the most volatility occurs when the data misses analyst expectations by a wide margin.<br/>
            <br/>
            If the actual data is better than the forecast, the currency typically appreciates. If the release comes in worse than expected, the currency tends to depreciate.<br/>
            <br/>For example, if analysts expected the U.S. unemployment rate to be 5% for the prior month and the figure came in at 4.8%, the better than expected figure would typically boost the US dollar.
        </p>
        <h3 style="text-align: justify;font-size: 37px;">
            <strong>Strategies for Forex Trading the News</strong>
        </h3>
        <p style="text-align: justify;">
            There are several different approaches to trading the news. Firstly, some forex traders try to forecast what the result of the economic releases will be and place a trade prior to the release based on this.<br/>
            <br/>
            When predicting economic data there are sometime clues in prior economic releases. For example, in forecasting U.S. jobs data you can use the employment component of PMI reports. If the employment component of the three reports has increased from the previous month, it suggests that the number of new jobs created also increased.<br/>
            <br/>
            A second strategy of forex trading the news is to wait until the figure has been released and trade based on how the market typically reacts to such a scenario. For example, if U.S. retail sales beat expectations by a wide margin, you might sell EUR/USD, based on anticipated strength in the US dollar.<br/>
            <br/>A third forex news <a class="btn-t48 tutorials__btn-link" style="color: #ff561c;" href="trading_strategies.php" target="_blank">trading strategy</a>   does not take the fundamental data into account and simply looks at prior price. In other words, the forex trader does not begin with a directional bias. A common strategy of this kind is to trade breakouts from the prior range as entry levels. This can be done on both a short term intraday basis and a daily basis.
        </p>
        <p style="text-align: justify;">For example, if the prior range in EUR/USD was a high of 1.2530 and a low of 1.2405, a trader might put a buy stop order in at 1.2530 and a sell stop order in at 1.2405. This is done with the expectation that if price breaks out above 1.2530 it will continue to rise, and conversely if it breaks below 1.2405 that it will continue to fall.</p>
