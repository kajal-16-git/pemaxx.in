        </p>
        <h2 style="text-align: justify;font-size: 35px;">European (London) session</h2>
        <p style="text-align: justify;">When financial centers through Asia are about to close, European markets start their day. Since European session coincides with both Asian and American ones, there is normally an increase in volatility and market liquidity, however, spreads tend to be more tight during the London session.</p>
        <p style="text-align: justify;">
            <img style="display: block; margin-left: auto; margin-right: auto; height: 350px;
