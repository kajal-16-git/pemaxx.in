        <h2 style="text-align: justify; font-size: 35px;">Asian (Tokyo) session</h2>
        <p style="text-align: justify;">With major trading center in Tokyo, Asian session also includes China, Australia, New Zealand and Russia. The first financial center to open after the weekend is actually Wellington, New Zealand, while Tokyo capital market itself only opens at 2 AM EET (3 AM EEST). Closing hours overlap with the beginning of the European session.&nbsp;</p>
