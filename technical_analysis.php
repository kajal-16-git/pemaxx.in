        <p class="normal">Technical analysis is a method of price forecasting that involves pattern recognition on a chart. Analysts employ various tools to identify levels of support and resistance, breakouts and breakdowns, trends and trading ranges. Knowing the strategies basics, one can likely find oneself able to implement some of the key elements into a self-designed strategy.</p>
        <h3 style="font-size: 34px;">Charts</h3>
        <p class="normal">
            A chart is a graphic representation of how the price changes within the set period of time. In almost any <a href="https://crm.pemaxx.com/register" style="color: #ff561c;">trading platform</a>
            you will find candlestick, bar and line chart types. All three are based on the same data but display them in different ways.
        </p>
        <ul class="rules" style="list-style-type: circle;">
            <li>Line chart is a simple and basic type that only shows closing prices.</li>
            <li>On the bar chart you can observe open, high, low and closing prices for each period of time. The vertical line is created by high and low prices, the dash on the left shows the open price and the dash on the right represents the close price.</li>
            <li>Perhaps the most popular type, candlestick chart shows open, high, low and closing prices during the set period of time as well. Each candlestick consists of the &ldquo;body &rdquo;created by open and closing prices and the &ldquo;wicks &rdquo;that show high and low prices for each period. This type of chart is usually displayed in two different colours - one represents bullish candlesticks while the other represents bearish.&nbsp;Bullish candlestick means that close price was higher than open price while bearish candlestick represents the opposite - close price was lower than open price.&nbsp;</li>
        </ul>
        <p class="normal">
            <img style="display: block; margin-left: auto; margin-right: auto;height: 355px;width: 690px;" src="assets/images/gallery/charts.png" alt="Forex Line - Bar - Candlestick charts" width="600" height="300"/>
        </p>
        <p class="normal">Note, however, that all of the charts described above show bid price only and you should not rely on them to identify where the ask price was at any given time.</p>
        <h3 style="font-size: 34px;">Timeframes</h3>
        <p class="normal">Time frame denotes the amount of time it takes to complete each candle or bar and how much data it includes. For example, time frame H1 shows how much the bid price fluctuated within an hour. You can customize time frame for each chart in your <a class="btn-t48 tutorials__btn-link" style="color: #ff561c;" href="https://crm.pemaxx.com/register" target="_blank">trading platform</a> .</p>
        <p class="normal">In general, shorter times frames are believed to produce more signals, however a significant part of them tends to be false. In contrast, longer time frames may provide relatively less signals but they will be stronger and more significant for a particular trend.</p>
        <p class="normal">Here is how the same price data look when you change periodicity:</p>
        <p class="normal">
