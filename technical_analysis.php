        </p>
        <h3 style="font-size: 34px;">Trend</h3>
        <p class="normal">Identifying the trend or the direction the market moves towards is one of the basic techniques in the analysis. Occasionally it can be determined by simply looking at the chart. Other cases will require more profound analysis of the price data.</p>
        <p class="normal">There are two major types of market trends:</p>
        <ul class="rules" style="list-style-type: circle;">
            <li>Uptrend - a series of escalating highs and lows;</li>
            <li>Downtrend - a series of lower highs and lower lows on chart .&nbsp;</li>
        </ul>
        <p class="normal">A lack of any particular direction is occasionally referred as to sideways or horizontal trend.&nbsp;</p>
        <p class="normal">
            <img style="display: block; margin-left: auto; margin-right: auto; height: 460px;    width: 690px;" src="assets/images/gallery/article4_3.png" alt="Forex trends" width="600" height="400" ">
        </p>
        <p class="normal">To identify a trend you can simply draw a straight line in the direction of the price moves on a chart. "Trend lines" are available in almost every trading platform and may be considered one of the beginner-friendly technical analysis tools. Another option is a technical indicator that can determine and display a trend when added to a chart.<a class="btn-t48 tutorials__btn-link" style="color: #ff561c;" href="Forex_Trading.php" target="_blank">here</a> </p>
        <h3 style="font-size: 34px;">Support and resistance</h3>
        <p class="normal">Finding support and resistance levels allows to determine when and in which direction should a position be opened and the potential profit or loss may be. Support is the price level which an asset has difficulty going below and resistance denotes the level which the pair has a difficulty rising above. These levels, however, do not always hold and a "breakout" or a &ldquo;breakdown &rdquo;occasionally occurs in one direction or another.</p>
        <p class="normal">Support and resistance levels form a trading range - a horizontal corridor that contains price fluctuations during a period of time.</p>
        <p class="normal">
            <img style="display: block; margin-left: auto; margin-right: auto; height: 312px;
    width: 660px;" src="assets/images/gallery/article4_4.png" alt="Technical analysis: Support and Resistance levels" width="600" height="340"/>
        </p>
        <p class="normal">A price movement through the identified level of resistance is referred to as breakout. Its bearish counterpart is called breakdown - a price movement through the identified level of support. Both breakout and breakdown are usually followed by increase in volatility <a class="btn-t48 tutorials__btn-link" style="color: #ff561c; font-size: 20px;" href="trading_education.php" target="_blank">here.</a></p>
        <p class="normal">To identify support and resistance you can simply mark the levels where the price had difficulty rising above and falling below in the past. Various technical indicators (i.e. Fibonacci or Pivot Points) can determine and draw the levels on the chart automatically.&nbsp;</p>
        <h3 style="font-size: 34px;">Chart patterns</h3>
        <p class="normal">Chart pattern is a distinct formation that predicts future price movement or creates a buy or sell signal. The theory behind it is basedon the assumption that certain patterns observed previously indicate where the price is currently headed.</p>
        <ul class="rules" style="list-style-type: circle;">
            <li>
                Head and Shoulders is considered to be one of the most reliable chart patterns which signifies that the trend is about to change. There are two types of this pattern - head and shoulders top that shows that upward movement may soon end and head and shoulders bottom, which means that downtrend is about to reverse.&nbsp;<img src="assets/images/gallery/head_and_shoulders.png" alt="Technical analysis: Chart patterns" width="600" height="380"/>
            </li>
            <li>
                Doji &ndash;is a candle with a short body (which means that the candle opened and closed at almost the same price) and relatively long wicks on each side that show market volatility during a period of time. Doji usually signifies market indecision since neither bullish nor bearish trend prevails.<img src="assets/images/gallery/doji.png" alt="Doji candle" width="600" height="260"/>
            </li>
            <li>
                Bullish hammer - a candle that usually occurs at a turn of the downtrend. This candle must have wicks twice as long as the body.<img src="assets/images/gallery/bullish_hammer.png" alt="Bullish hammer" width="600" height="330"/>
            </li>
            <li>
                Hanging man - bearish counterpart of bullish hammer that has a shorter body and long wicks and is usually found at the before the reversal of the uptrend.&nbsp;&nbsp;<br/>
                <img src="assets/images/gallery/hanging.png" alt="Hanging man" width="600" height="260"/>
            </li>
            <li>
                Another popular chart pattern is the triangle. There are three types of triangles: symmetrical, ascending and descending. The symmetrical triangle is a pattern where two trend lines that meet at one point and neither of them is flat. This pattern usually confirms the direction of the current trend. In an ascending triangle, the upper trendline is flat and the lower one is headed upwards. This pattern is considered to be bullish and may predict a breakout. Descending triangle has a flat lower line and the upper trendline is descending. Descending triangle is a bearish pattern signifying an upcoming breakdow<em>n.</em>
                &nbsp;
            </li>
        </ul>
        <h3>Indicators</h3>
        <p class="normal">
            One of the tools that allows to predict or confirm trends, patterns, support and resistance levels or buy and sell signals is a technical indicator. It is a software developed specifically for your<a href="https://crm.pemaxx.com/register" style="color: #ff561c;">trading platform</a>
            that makes calculations based on price movements and volatility. Both cTrader and MT5
            have a wide range of readily available indicators, however you can always download a custom one or even create it yourself.
        </p>
        <p class="normal">Simply adding an indicator to a price chart may greatly extend your understanding of the current market situation and help to decide in which direction you should be trading. For instance, to identify support and resistance levels, such indicators as Fibonacci or Pivot Points may come in handy. Momentum indicator will help you to measure the rate of price change and Zig Zag can be used to predict when the trend will be more likely to reverse.</p>
        <p class="normal">
            To learn more on how indicators can be installed and customized, please check instructions for MT5 or cTrader in our Manuals section
            .&nbsp;
        </p>
