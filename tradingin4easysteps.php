        <h2 dir="ltr" style="text-align: justify; font-size: 35px;">1. Learn about Forex trading</h2>
        <p dir="ltr" style="text-align: justify;">
            Watch Forex trading course
        </p>
        <p dir="ltr" style="text-align: justify;">
            If you &rsquo;re a beginner, there are certain concepts and terms that you need to get familiar with. We &rsquo;ve covered them in the <a href="how_to_tread.php" style="color:#ff561c;">How to trade Forex</a>
            article. You can also explore our <a href="education.php" style="color:#ff561c;">Education section</a>
            . It &rsquo;ll help you expand your knowledge about the market in general and our services in particular. If you feel like practising with virtual funds before switching to real trading, you can open an Pemaxx &nbsp;<a href="Demo_account.php" style="color:#ff561c;">demo account</a>
            . Let &rsquo;s start with some important Forex concepts.
        </p>
        <p dir="ltr" style="text-align: justify;">
            Margin is the amount of funds required to open new and maintain open positions.&nbsp;It is different for each instrument. You can use our Trading Calculator
            to figure out your required margin. If you don't have enough funds (for example, if your floating loss gets too high)&mdash;you might be asked to top up your account to keep your orders. This event is known as <strong>margin call</strong>
            .
        </p>
        <p dir="ltr" style="text-align: justify;">Pip is a standardised unit of price change. 1 pip is calculated by the second to last digit. For example, when the price for the EURUSD currency pair changes from 1.11634 to 1.11645, it means the price has changed by 1.1 pips. For the USDJPY currency pair, which has 3-digit pricing, a change from 123.857 to 123.864 means that the price has grown by 0.7 pips.</p>
        <p dir="ltr" style="text-align: justify;">
            <a href="Leverage_Forex.php"style="color:#ff561c;">Leverage</a>
            reduces marginal requirements, the amount necessary to maintain a certain position, and helps you open orders with a volume larger than your balance would allow otherwise. It is important to note that the higher the volume of your order, the more you gain or lose.&nbsp;&nbsp;
        </p>
        <p dir="ltr" style="text-align: justify;">Let &rsquo;s say you have a trading account with 500 USD and a 1:500 leverage applied. You decide to open a position for 1 lot (100,000 units) on EURUSD, when the price is at 1.13415. The required margin for this position is 226.830 USD, almost half of your funds. Each pip movement is then worth 10 USD. Therefore, the price only needs to drop to 1.13145 for you to lose nearly all money in your account. If you open a position for 0.5 lots, each pip will cost you only 5 USD. In this case, if the price falls to 1.13145, your loss will amount to 135 USD.</p>
        <p dir="ltr" style="text-align: justify;">How much leverage should you use? It depends on the level of risk you &rsquo;re willing to take. Higher leverage means higher risk and reward. You can change it at any time as you learn.</p>
