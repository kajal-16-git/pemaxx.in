            <a href="trading_strategies_goals.php" style="color:#ff561c;">Compare trading strategies</a>
        </p>
        <p dir="ltr" style="text-align: justify;">As a beginner, you can simply track the general direction of the price on the chart and open Buy orders when it goes up or Sell orders when it goes down. This may not get you a guaranteed profit every time, however, it is a good start for developing your strategy.</p>
        <p dir="ltr" style="text-align: justify;">There are three types of trends you should look for: uptrend, downtrend, and sideways trend. If you notice a trend, you should mark it with lines as shown on the picture below. The closer the price is to one of the lines &mdash;the more likely it is to go in the opposite direction.</p>
