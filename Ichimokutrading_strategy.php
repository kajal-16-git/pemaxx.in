        <div>
                <h6>Five main components of the Ichimoku Cloud</h6><br>
                <p>The Ichimoku Kinko Hyo technical indicator is a collection of five different lines.</p>
         </div>
           <div>
       <ul style="list-style-type:disc;margin-left:20px; font-size: 18px;">
                <li><b>Conversion Line (Tenkan-Sen):</b> a short-term moving average calculated from the highest and the lowest prices over the past nine periods.</li>
                <li><b>Base Line (Kijun-Sen):</b> a medium-term moving average similar to the Conversion Line but based on the past 26 periods.</li>
                <li><b>Lagging Span (Chikou Span)</b>:the closing price with a 26-period lag.</li>
                <li><b>Lead 1 (Senkou Span A or Leading Span A):</b> the midpoint between the Conversion Line and the Base Line projected 26 periods into the future.</li>
                <li><b>Lead 2 (Senkou Span B or Leading Span B)</b> a medium-term moving average of the past 52 periods projected 26 periods ahead.</li>
            </ul>
  </div><br>
       <div>
