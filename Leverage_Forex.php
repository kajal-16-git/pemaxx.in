        <p dir="ltr" style="text-align: left;">Forex leverage refers to the ability to control larger positions with a relatively smaller amount of capital. Essentially, traders borrow funds from their broker to enter positions that exceed their account balance.&nbsp;</p>
        <p dir="ltr" style="text-align: left;">The leverage ratio determines the amount of borrowed funds traders can access. For example, a leverage ratio of 1:100 means that for every dollar of capital, traders can control $100 worth of trading positions.&nbsp;</p>
        <p dir="ltr" style="text-align: left;">Traders earn by taking advantage of fluctuations in currency pairs. The profit or loss in Forex is typically measured in pips, which is the smallest unit of price movement in a currency pair. The pip value represents the monetary value of each pip movement. The larger the position size, the greater the pip value, which means that each pip movement will result in a higher profit or loss. Therefore, the larger positions traders open, the higher their potential profits can be. Since utilising leverage allows traders to control larger positions in the market, leveraged trading can potentially amplify their profits.</p>
        <p dir="ltr" style="text-align: left;">
            Leveraged trading provides a lower initial capital requirement, enabling traders to invest in assets with higher nominal values or requiring larger investments. For example, with Pemaxx, you can trade <!-- <a href="currencypair.php"style="color: #ff561c;"> -->currency pairs
            with a maximum leverage of 1:1000, commodities
            with 1:400, indices
            with 1:400, and stocks
            with 1:40.&nbsp;
        </p>
        <p dir="ltr" style="text-align: left;">The maximum leverage that Pemaxx offers is 1:1000, meaning that you can hold a position 1000 times larger than your initial investment and potentially earn 1000 times more.</p>
        <h2 dir="ltr" style="text-align: left;font-size: 30px;">Example of how leverage works in Forex</h2>
        <p dir="ltr" style="text-align: left;">Suppose you have a trading account with a balance of $1,000, and you decide to utilise leverage with a ratio of 1:100. With this leverage, you can control positions worth $100,000 in the <a class="btn-t48 tutorials__btn-link" style="color: #ff561c;" href="live_market.php" target="_blank">market.</a></p>
        <p dir="ltr" style="text-align: left;">Without leverage, your trading account balance of $1,000 would limit you to trading smaller positions. However, with leverage, you can enter trades with a much larger volume.</p>
        <p dir="ltr" style="text-align: left;">Let's say you want to trade the EURUSD currency pair, which is currently priced at 1.2000. Without leverage, with your $1,000 balance, you would only be able to place an order worth 833 EUR (1,000 / 1.2000 = 833.33).</p>
        <p dir="ltr" style="text-align: left;">However, with the leverage of 1:100, you can control a position size of $100,000. You can open a trade for 83,333 EUR (100,000 / 1.2000 = 83,333.33).</p>
        <p dir="ltr" style="text-align: left;">Now, let's assume the EURUSD price moves in your favour, increasing by 100 pips (0.0100). If you were trading without leverage, your profit would be relatively small: 100 pips * 833 EUR = 8.33 EUR.</p>
        <p dir="ltr" style="text-align: left;">With leverage, your profit is calculated based on the total position size, so your profit would be 100 pips * 83,333 EUR = 833.33 EUR.</p>
        <h2 dir="ltr" style="text-align: left;font-size: 30px;">Margin requirements for leveraged trading</h2>
        <p dir="ltr" style="text-align: left;">It is impossible to separate the concept of leverage in trading from that of margin. Margin is the minimum amount of money that traders need to deposit with their broker to be allowed to open new positions, including leveraged ones, and keep the initiated orders open. It is a form of security or a &lsquo;good faith &rsquo;deposit that ensures traders can cover potential losses from their trades. The margin required by brokers is typically expressed as a percentage of the total trade size.</p>
        <p dir="ltr" style="text-align: left;">
            The margin requirement depends on the currency pair, volume, and account leverage. Use our <a href="https://crm.pemaxx.com/register" style="color: #ff561c;">Pemaxx Trading </a>
            to determine the required margin for your orders.
        </p>
        <h3 dir="ltr" style="text-align: left;font-size: 30px;">Margin call &nbsp;</h3>
        <p dir="ltr" style="text-align: left;">Maintaining adequate margin levels is crucial for traders engaged in leveraged trading. Falling below the required margin level leads to a margin call. The event occurs when the account equity (= Account Balance + Unrealised Profits/Losses + Realised Profits/Losses) falls below the required margin level, indicating that the trader does not have sufficient funds to cover potential losses.&nbsp;</p>
        <p dir="ltr" style="text-align: left;">When a broker notifies a trader of the margin call, the trader should deposit more money in their trading account to increase their margin or close losing positions and fix the loss in order to free up more margin.&nbsp;</p>
        <p dir="ltr" style="text-align: left;">At Pemaxx, in the event of a margin call, you &rsquo;ll receive an email and a notification in the Pemaxx Trading App. A margin call is triggered when your margin level hits 25%.</p>
        <p dir="ltr" style="text-align: left;">To avoid margin calls, you should carefully monitor your positions, maintain adequate account equity, and ensure a healthy margin level.</p>
        <p dir="ltr" style="text-align: left;">A margin call is a warning for you to take action. If no action is taken, Stop Out occurs next.</p>
        <h3 dir="ltr" style="text-align: left;font-size: 30px;">Stop Out &nbsp;</h3>
        <p dir="ltr" style="text-align: left;">A margin call typically occurs before the Stop Out level is reached. Stop Out is a risk control mechanism implemented by brokers to protect traders and maintain market stability. When the account equity reaches a predetermined Stop Out level, the broker automatically closes the trader's positions. This is likely to happen if a trader doesn &rsquo;t take any action as soon as he receives a margin call.&nbsp;</p>
        <p dir="ltr" style="text-align: left;">Stop Out is calculated based on all open positions. The broker will close your positions one by one, starting from the one with the largest loss, until your account equity rises above the Stop Out level. In other words, if you have any losing position, it can lead to the closure of all positions, including profitable ones, until the margin reaches the required level.</p>
        <p dir="ltr" style="text-align: left;">Traders must be aware of the Stop Out level set by their broker and take necessary precautions to ensure their account remains above this threshold. At Pemaxx, Stop Out happens at a 15% margin level.</p>
        <h3 dir="ltr" style="text-align: left;font-size: 30px;">How to choose trading account leverage ratio</h3>
        <p dir="ltr" style="text-align: left;">
            Traders can use a trading calculator or a <!-- <a href="../../../markets/forex-calculator/"> -->Forex margin calculator</a>
            to determine the optimal leverage ratio for their trading account. By inputting the desired leverage ratio, traders can calculate the maximum position size they can trade without risking a margin call.&nbsp;
        </p>
        <p dir="ltr" style="text-align: left;">Keep in mind that there is not one best leverage to use in Forex. It is important to consider personal risk tolerance, <a class="btn-t48 tutorials__btn-link" style="color: #ff561c;font-size: 20px;" href="trading_strategies.php" target="_blank">trading strategies</a>, and market conditions when choosing the leverage ratio.</p>
